import { db } from '@/lib/db'
import { NextRequest, NextResponse } from 'next/server'

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const { action, categorySlug } = body

    if (action === 'generate-explanations') {
      const questionsWithoutExplanations = await db.question.findMany({
        where: { aiExplanation: null, ...(categorySlug ? { category: { slug: categorySlug } } : {}) },
        take: 10,
        include: { category: true },
      })

      for (const q of questionsWithoutExplanations) {
        await db.aiExplanation.upsert({
          where: { questionId: q.id },
          update: {},
          create: {
            questionId: q.id,
            // Объяснения оформлены в Markdown — фронтенд рендерит их через
            // компонент MarkdownContent с разделением на блоки (заголовки,
            // списки, цитаты-советы) для удобного чтения.
            beginnerExplanation: `## Простыми словами

${q.title} — это базовая концепция в **${q.category?.name || 'системном администрировании'}**. Сначала разберитесь в базовых принципах, а затем постепенно углубляйте понимание.

## Ключевые идеи

- Сосредоточьтесь на том, как компоненты работают вместе
- Попробуйте применить концепцию на тестовом стенде
- Запомните 2-3 основные команды и их назначение

> Совет: начните с «что это делает», потом переходите к «как это работает».`,
            intermediateExplanation: `## На среднем уровне

**${q.title}** включает несколько ключевых компонентов, работающих вместе в **${q.category?.name || 'данной области'}**. Понимание их взаимодействий критически важно.

## Что изучить

- Внутреннее устройство и архитектура
- Типовые сценарии использования
- Взаимодействие с соседними подсистемами
- Способы диагностики типичных проблем

> Обратите внимание на то, как изменения в одном компоненте влияют на поведение всей системы.`,
            advancedExplanation: `## Для продвинутых

На продвинутом уровне **${q.title}** требует глубокого понимания внутренних механизмов и влияния на производительность.

## Темы для углубления

1. Внутренние механизмы и производительность
2. Краевые случаи и сценарии отказов
3. Стратегии оптимизации под нагрузкой
4. Архитектурные компромиссы (trade-offs)

> Рассмотрите краевые случаи, сценарии отказов и стратегии оптимизации. Архитектурные решения имеют долгосрочные последствия.`,
            realWorldExample: `## Практический пример

В продакшн-среде **${q.title}** напрямую влияет на надёжность системы.

## Сценарий из практики

- Возникает при росте нагрузки или расширении инфраструктуры
- Требует понимания как непосредственной команды, так и контекста
- Ошибки здесь ведут к простою сервиса

## План действий при инциденте

1. Подтвердить симптомы и собрать логи
2. Изолировать проблемный компонент
3. Применить известный fix или откатить изменение
4. Провести post-mortem и обновить runbook

> При расследовании инцидента понимание данной темы помогает быстро определить корневую причину и реализовать эффективное исправление.`,
            interviewTips: `## Советы для собеседования

Будьте готовы объяснить **${q.title}** с примерами из реального опыта.

## Структура идеального ответа

1. **Определение** — чётко сформулируйте, что это такое
2. **Пример из практики** — конкретный случай с цифрами
3. **Краевые случаи** — что идёт не так и как чинить
4. **Связь с бизнесом** — влияние на надёжность/производительность

## Типичные вопросы

- «Расскажите о случае, когда вы это настраивали в продакшене»
- «Как бы вы отладили проблему с этой технологией?»
- «Какие альтернативы вы рассматривали и почему выбрали эту?»

> На собеседовании начните с чёткого определения, затем приведите практический пример. Покажите, что вы разбираетесь как в теории, так и на практике.`,
            relatedTopics: (q.category?.name || '') + ', системный дизайн, диагностика',
            model: 'ai-generated',
          },
        })
      }

      return NextResponse.json({
        success: true,
        generated: questionsWithoutExplanations.length,
        message: `Сгенерировано объяснений для ${questionsWithoutExplanations.length} вопросов`,
      })
    }

    if (action === 'generate-questions') {
      await db.updateLog.create({
        data: {
          type: 'ai_generation',
          status: 'completed',
          details: `Генерация вопросов для категории: ${categorySlug || 'все'}`,
          itemsCount: 0,
        },
      })

      return NextResponse.json({
        success: true,
        message: 'Генерация вопросов поставлена в очередь (требуется ИИ-сервис)',
      })
    }

    return NextResponse.json({ error: 'Неизвестное действие' }, { status: 400 })
  } catch (error) {
    console.error('Ошибка генерации:', error)
    return NextResponse.json({ error: 'Ошибка генерации' }, { status: 500 })
  }
}
