function generateInterviewResponse(message: string, context?: string): string {
  if (message.length < 20) {
    return `Ваш ответ слишком краткий. На собеседовании стоит расписать подробнее.

## Советы по структуре ответа

1. **Начните с определения** — покажите, что понимаете концепцию
2. **Приведите практический пример** — продемонстрируйте опыт
3. **Упомяните краевые случаи** — покажите глубину знаний
4. **Свяжите с бизнес-эффектом** — покажите, что мыслите шире техники

> Минимальный объём хорошего ответа — 3-4 предложения с конкретикой.

Попробуйте расширить ответ!`
  }

  if (message.length < 50) {
    return `Ответ в правильном направлении, но нужна большая глубина.

## Оценка: 5/10

### Сильные стороны

- Вы определили ключевую концепцию

### Что улучшить

- Добавьте конкретные примеры или команды
- Объясните «почему», а не только «что»
- Упомяните альтернативы и компромиссы

> Идеальный ответ на собеседовании — 100-200 слов с примерами из практики.

Попробуйте ответить ещё раз с большей детализацией!`
  }

  return `Хороший ответ! Вы демонстрируете уверенное понимание.

## Оценка: 8/10

### Сильные стороны

- Чёткое объяснение концепции
- Хорошие примеры
- Виден практический опыт

### Чтобы сделать идеальным

- Упомяните влияние на производительность
- Обсудите сценарии отказов
- Добавьте подход к мониторингу/отладке

> На собеседовании уровня Senior ждут обсуждения trade-offs и реальных инцидентов из практики.

Продолжайте в том же духе! Вы хорошо подготовлены к собеседованиям.`
}

