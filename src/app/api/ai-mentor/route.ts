import { NextRequest, NextResponse } from 'next/server'

/**
 * ИИ-наставник — генератор ответов для режима собеседования и объяснений.
 * Все ответы возвращаются в формате Markdown — фронтенд рендерит их через
 * компонент MarkdownContent с разделением на блоки (заголовки, списки, код).
 *
 * Правила форматирования ответов:
 *  - Используем ## для заголовков секций (рендерятся с акцентной полосой слева)
 *  - Используем ### для подсекций
 *  - Используем - для маркированных списков (с цветными буллетами)
 *  - Используем 1. для нумерованных списков (с круглыми бейджами)
 *  - Используем ```bash для блоков кода (с кнопкой копирования)
 *  - Используем `code` для инлайн-команд
 *  - Используем > для цитат-советов
 *  - Параграфы разделяем пустой строкой для визуального воздуха между блоками
 */

export async function POST(request: NextRequest) {
  try {
    const body = await request.json()
    const { message, context, mode } = body

    let response = ''

    if (mode === 'interview') {
      response = generateInterviewResponse(message, context)
    } else if (mode === 'explain') {
      response = generateExplanationResponse(message, context)
    } else {
      response = generateMentorResponse(message, context)
    }

    return NextResponse.json({ response })
  } catch (error) {
    console.error('Ошибка ИИ-наставника:', error)
    return NextResponse.json({ error: 'Ошибка ИИ-наставника' }, { status: 500 })
  }
}

